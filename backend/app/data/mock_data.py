def generate_score_rank_table(province_name: str, total_score: int, total_students: int):
    """
    Generate a simulated score-rank table for a province.
    Uses a normal distribution to approximate score distribution.
    Returns list of (score, cumulative_count) tuples.
    """
    mean = total_score * 0.55  # mean score around 55% of total
    std = total_score * 0.12

    result = []
    step = 5 if total_score >= 750 else 3

    # From max score down to ~100 points
    for score in range(total_score, 80, -step):
        # Normalized z-score
        z = (score - mean) / std
        # Survival function (1 - CDF) approximated
        percentile = 0.5 * math.erfc(z / math.sqrt(2))
        cumulative = max(1, int(percentile * total_students))
        result.append((score, cumulative))

    return result
